public class Attribute {

	private String Name;
	
	private Object value;

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public Object getValue() {
		return value;
	}

	public void setValue(Object value) {
		this.value = value;
	}
	
	
	
}
